	init(lang) {
		if (this.initialized) {
			return;
		}

		i18next.init({
			lng: lang || navigator.language || navigator.userLanguage,
			fallbackLng: ['cz', 'en'],
			resources: this.translations
		});

		this.initialized = true;
	}

	async changeLang(lang) {
		await i18next.changeLanguage(lang);
		ROUTER.resolveCurrentRoute();
	}

	_(key) {
		return i18next.t(key);
	}
